public class declarando_variaveis {

    public static void main(String[] args) {
        int um_numero = 5;
        float um_ponto_flutuante = 3.14f; // possui 4 bytes
        char uma_letra = 'c';
        double um_double = 5.6235; // possui 8 bytes
        boolean um_boleano = true; //diferente de python começa com minusculas
        String um_texto = "Meu nome é valter estou aprendendo Java, isso é magnifico";

        System.out.println(um_numero);
        System.out.println(um_ponto_flutuante);
        System.out.println(uma_letra);
        System.out.println(um_double);
        System.out.println(um_boleano);
        System.out.println(um_texto);
    }
    
}
